/* 2.16 (Aritmética) Escriba un programa que solicite al usuario que ingrese dos números, 
obtenga los dos números del usuario e imprima la suma, el producto,
 la diferencia y el cociente de los dos números. */
#include<iostream>
using namespace std;

int main() {
float x;
float y;
float recid;
recid = x / y;
cout << "Ingresa dos números: " ;
cin >> x >> y ;
cout << "suma = " << x + y ;;
cout << "\ndiferencia = " << x - y ;
cout << "\nproducto = " << x * y ;
cout << "\ncociente = " << recid ;

return 0 ;
}
